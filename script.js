const animatedObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("in-view");
        animatedObserver.unobserve(entry.target);
      }
    });
  },
  {
    threshold: 0.16,
    rootMargin: "0px 0px -30px 0px"
  }
);

let currentLang = "fr";
let currentFilter = "all";

function deepValue(object, path) {
  return path.split(".").reduce((acc, key) => {
    if (acc && Object.prototype.hasOwnProperty.call(acc, key)) {
      return acc[key];
    }
    return undefined;
  }, object);
}

function t(path) {
  return deepValue(translations[currentLang], path) ?? deepValue(translations.fr, path) ?? path;
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/\"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function bindAnimatedElements() {
  document.querySelectorAll("[data-animate]").forEach((element) => {
    if (element.dataset.observed === "true") {
      return;
    }
    element.dataset.observed = "true";
    animatedObserver.observe(element);
  });
}

function formatPrice(value) {
  const locale = currentLang === "ar" ? "ar-MA" : currentLang === "en" ? "en-US" : "fr-MA";
  return new Intl.NumberFormat(locale).format(value);
}

function renderMenu() {
  if (!menuGrid) {
    return;
  }

  const visibleItems = currentFilter === "all"
    ? menuItems
    : menuItems.filter((item) => item.category === currentFilter);

  if (!visibleItems.length) {
    menuGrid.innerHTML = `<p class="menu-empty">${escapeHtml(t("menu.empty"))}</p>`;
    return;
  }

  menuGrid.innerHTML = visibleItems
    .map((item) => {
      const title = item.title[currentLang] ?? item.title.fr;
      const description = item.description[currentLang] ?? item.description.fr;
      const categoryLabel = t(`categories.${item.category}`);

      return `
        <article class="menu-card" data-animate>
          <div class="menu-card-header">
            <h3>${escapeHtml(title)}</h3>
            <p class="price">MAD ${formatPrice(item.price)}</p>
          </div>
          <span class="category">${escapeHtml(categoryLabel)}</span>
          <p>${escapeHtml(description)}</p>
        </article>
      `;
    })
    .join("");

  bindAnimatedElements();
}

function setActiveFilter(nextFilter) {
  currentFilter = nextFilter;

  filterButtons.forEach((button) => {
    const isActive = button.dataset.filter === nextFilter;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderMenu();
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  document.documentElement.dir = currentLang === "ar" ? "rtl" : "ltr";
  document.title = t("meta.title");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = t(key);
    element.textContent = value;
  });

  dayCells.forEach((element) => {
    const label = t(`days.${element.dataset.day}`);
    element.textContent = label;
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === currentLang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderMenu();
}

function closeMobileMenu() {
  document.body.classList.remove("nav-open");
  if (menuToggle) {
    menuToggle.setAttribute("aria-expanded", "false");
  }
}

function setupNavigation() {
  if (!menuToggle) {
    return;
  }

  menuToggle.addEventListener("click", () => {
    const opening = !document.body.classList.contains("nav-open");
    document.body.classList.toggle("nav-open", opening);
    menuToggle.setAttribute("aria-expanded", String(opening));
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      closeMobileMenu();
    });
  });

  document.addEventListener("click", (event) => {
    if (!document.body.classList.contains("nav-open")) {
      return;
    }

    if (!topbar.contains(event.target)) {
      closeMobileMenu();
    }
  });

  window.addEventListener("resize", () => {
    if (window.innerWidth > 860) {
      closeMobileMenu();
    }
  });
}

function setupScrollingNav() {
  let previousY = window.scrollY;

  window.addEventListener(
    "scroll",
    () => {
      const currentY = window.scrollY;

      if (currentY > 140 && currentY > previousY + 6 && !document.body.classList.contains("nav-open")) {
        topbar.classList.add("topbar-hidden");
      } else {
        topbar.classList.remove("topbar-hidden");
      }

      previousY = currentY;
    },
    { passive: true }
  );
}

function setupFilters() {
  filterButtons.forEach((button) => {
    button.addEventListener("click", () => {
      setActiveFilter(button.dataset.filter);
    });
  });
}

function setupLanguages() {
  langButtons.forEach((button) => {
    button.addEventListener("click", () => {
      currentLang = button.dataset.lang;
      applyTranslations();
      closeMobileMenu();
    });
  });
}

